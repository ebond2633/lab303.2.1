public class AddTwoDoubles {
    public static void main(String[] args) {
        // Declare and initialize two double variables
        double num1 = 10.5;
        double num2 = 20.3;

        // Calculate the sum
        double sum = num1 + num2;

        // Print the result
        System.out.println("Sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}
